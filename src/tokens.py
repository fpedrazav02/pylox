@dataclass
class Token:
    _line: int
    _tokenType: TokenType
    _literal: object
    _lexeme: str

    @override
    def __str__(self) -> str:
        return f"Line: {self._line} - Token<Type: {self._tokenType}, Lexeme: {self._lexeme}, Literal: {self._literal}>"

    @override
    def __repr__(self) -> str:
        return self.__str__()
